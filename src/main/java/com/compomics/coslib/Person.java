package com.compomics.coslib;



/**
 *
 * @author Genet
 */
public class Person {
    
    String name;
    int age;
    String address;
    
    public int getage(){
        return this.age;
    }
    
    public void setage(int a){
        this.age=a;
    }
    
    public String getname(){
        return this.name;
    }
    
    public void setname(String n){
        this.name=n;
    }
    
    public String getaddress(){
        return this.address;
    }
    
    public void setaddress(String addr){
        this.address=addr;
    }
    
    
}
